    glGenFramebuffers(1, &this->captureFBO);
    glGenRenderbuffers(1, &this->captureRBO);
    glBindFramebuffer(GL_FRAMEBUFFER, this->captureFBO);
    glBindRenderbuffer(GL_RENDERBUFFER, this->captureRBO);
